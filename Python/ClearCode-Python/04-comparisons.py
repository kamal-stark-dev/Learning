# Comparison Operators in Python

# Equal to
a = 5
b = 5
print(a == b)  # True

# Not equal to
a = 5
b = 3
print(a != b)  # True

# Greater than
a = 5
b = 6
print(a > b)  # False

# Less than
a = 3
b = 5
print(a < b)  # True

# Greater than or equal to
a = -2
b = 5
print(a >= b)  # True

# Less than or equal to
a = 3
b = 5
print(a <= b)  # True