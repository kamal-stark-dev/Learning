# Assignment operators in Python

# Assigning a value to a variable
x = 10
print("x =", x)

# Add and assign
x += 5  # equivalent to x = x + 5
print("x += 5 ->", x)

# Subtract and assign
x -= 3  # equivalent to x = x - 3
print("x -= 3 ->", x)

# Multiply and assign
x *= 2  # equivalent to x = x * 2
print("x *= 2 ->", x)

# Divide and assign
x /= 4  # equivalent to x = x / 4
print("x /= 4 ->", x)

# Modulus and assign
x %= 3  # equivalent to x = x % 3
print("x %= 3 ->", x)

# Floor division and assign
x //= 2  # equivalent to x = x // 2
print("x //= 2 ->", x)

# Exponent and assign
x **= 3  # equivalent to x = x ** 3
print("x **= 3 ->", x)

